package src;

/**
 * Represents the possible states of an object in the system.
 * Each state corresponds to a distinct behavior or condition.
 *
 * Normal: The monsters operates under normal conditions.
 * Freeze: The state where the monsters are temporarily frozen.
 * Furious: The state where the monsters get furious.
 */
public enum State {
    Normal,
    Freeze,
    Furious;
}